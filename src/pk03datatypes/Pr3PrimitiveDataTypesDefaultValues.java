package pk03datatypes;

public class Pr3PrimitiveDataTypesDefaultValues {

	byte b;
	short s;
	int i;
	long l;
	float f;
	double d;
	boolean bo;
	char ch;
	
	public static void main(String[] args) {
		Pr3PrimitiveDataTypesDefaultValues tc = new Pr3PrimitiveDataTypesDefaultValues();
		
		System.out.println("byte default value is "+tc.b);
		System.out.println("short default value is "+tc.s);
		System.out.println("int default value is "+tc.i);
		System.out.println("long default value is "+tc.l);
		System.out.println("float default value is "+tc.f);
		System.out.println("double default value is "+tc.d);
		System.out.println("boolean default value is "+tc.bo);
		System.out.println("char default value is "+tc.ch);
	}

}
