package pk04operators;

public class Pr1ArithmaticOperators {

	public static void main(String[] args) {
		int a = 10;
		int b = 9;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println((float)a/b);
		System.out.println(a%b);
	}

}

