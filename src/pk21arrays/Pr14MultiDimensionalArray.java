package pk21arrays;

public class Pr14MultiDimensionalArray {

	public static void main(String[] arr) {
		
		int[][] testarray = new int[2][2];
		
		testarray[0][0] = 1;
		testarray[0][1] = 2;
		testarray[1][0] = 3;
		testarray[1][1] = 4;

		System.out.println(testarray[0][0]);
		System.out.println(testarray[0][1]);
		System.out.println(testarray[1][0]);
		System.out.println(testarray[1][1]);
		
	}
}
